radio = int (input ("Escribe el valor del radio: ")) 
pi = 3.1416

# PROCESOS
perimetro = 2*pi*radio
area = pi*radio**2

# SALIDA DE DATOS
print("\nEl perimetro del circulo es: ", round(perimetro,2))
print("El area del circilo es: ", round(area,2))